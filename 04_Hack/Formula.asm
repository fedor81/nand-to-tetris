// RAM[4] = (RAM[0] * 3 + (RAM[1] | RAM[2])) & !RAM[3] + 11

// Вычисление RAM[1] | RAM[2]
@1
D = M
@2
D = D | M    // RAM[1] | RAM[2]
@4
M = D   // Результат в RAM[4]


// Вычисление RAM[0] * 3 
@0
D = M
D = D + M
D = D + M    // Результат в D


// Складываем (RAM[0] * 3) + (RAM[1] | RAM[2])
@4
D = D + M
M = D   // Результат в RAM[4]


// Выполняем RAM[4] & !RAM[3]
@3
D = !M
@4
M = D & M   // Результата в RAM[4]


// Прибавляем 11
@11
D = A
@4
M = D + M   // Результат в RAM[4]
